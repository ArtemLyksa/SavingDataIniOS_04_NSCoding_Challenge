class OTRShow {

  var title: String
  var fileName: String
  var showDescription: String
  var showId: Int
  
  init(title:String, fileName:String, showDescription: String, showId: Int) {
    self.title = title
    self.fileName = fileName
    self.showDescription = showDescription
    self.showId = showId
  }
  

}
